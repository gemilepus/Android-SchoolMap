package com.vine.projectdemo.Model;

public class ServerResponse {

    private String result;
    private String message;
    private User user;
    private String token;

    public String getResult() {
        return result;
    }
    public String getMessage() {
        return message;
    }
    public User getUser() {
        return user;
    }
    public String getToken() {
        return token;
    }
}
